# Assigning values to variables
name = "John Doe"  # A string variable
age = 25           # An integer variable
height = 5.9       # A float variable (decimal number)

# Printing the variables
print("Name:", name)
print("Age:", age)
print("Height:", height, "feet")

# Basic operations
# Let's assume we want to increase the age by 1 and convert height into centimeters
age = age + 1  # Increasing age
height_cm = height * 30.48  # Converting feet to centimeters

# Printing the updated values
print("Updated Age:", age)
print("Height in centimeters:", height_cm)
