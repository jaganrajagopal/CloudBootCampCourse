# Assigning values to variables
a = 10
b = 3

# Addition
addition = a + b
print(f"The sum of {a} and {b} is {addition}.")

# Subtraction
subtraction = a - b
print(f"The difference between {a} and {b} is {subtraction}.")

# Multiplication
multiplication = a * b
print(f"The product of {a} and {b} is {multiplication}.")

# Division
division = a / b
print(f"The division of {a} by {b} results in {division}.")



# Modulus (remainder)
modulus = a % b
print(f"The remainder when {a} is divided by {b} is {modulus}.")

# Exponent
exponent = a ** b
print(f"{a} raised to the power of {b} is {exponent}.")
